
package eva1_6_operaciones;

public class EVA1_6_OPERACIONES {

    public static void main(String[] args) {
        //AREA DE UN CIRCULO 
        //DECLARACION DE VARIABLES
        double radio;
        double area;
        double pi;
        //INICIALIZACION
        pi = 3.1416;
        radio = 5;
        area = pi * radio *radio;
        System.out.println("EL AREA DEL CIRCULO DE RADIO 5 ES:");
        System.out.println(area);
        //NUEVO CRICULO RADIO 100
        radio = 100; //LOS VALORES LOS PODEMOS CAMBIAR Y CONSECUTIVO...
        area = pi * radio *radio;
         System.out.println("EL AREA DEL CIRCULO DE RADIO 100 ES:");
        System.out.println(area);



    }
    
}
