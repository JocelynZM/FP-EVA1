
package eva1_7_triangulo;

public class EVA1_7_TRIANGULO {

    public static void main(String[] args) {
       //AREA TRIANGULO 
       //AREA = (BASE * ALTURA)ENTRE 2
       double base, altura, area; //TRES VARIABLES TIPO DOUBLE
       //INIACILIZACION
       base = 11;
       altura = 3;
       //SE APLICA PRECEDENCIA DE OPERACIONES
       area = (base * altura)/2.0;//CUIDADO CON DIVIDIE ENTRE ENTEROS
       System.out.println("EL AREA DEL TRIANGULO BASE 11 Y ALTUTA 3 ES:");
       System.out.println(area);
       
        
        
        
    }
    
}
